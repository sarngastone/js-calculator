function add(num1, num2) {
    let sum = num1 + num2
    return sum
}

function subtract(num1, num2) {
    let sum = num1 - num2
    return sum
}
function multiply(num1, num2) {
    let sum = num1 * num2
    return sum
}
function divide(num1, num2) {
    let sum = num1 / num2
    return sum
}
